package com.lipomancer.wwrp.util;

/**
 * A two-dimensional integer vector.
 */
public class IntVector2 {

    public int x;
    public int y;

    /**
     * Gets the x and y values of this int vector.
     *
     * @param x The x value
     * @param y The y value
     */
    public IntVector2(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
